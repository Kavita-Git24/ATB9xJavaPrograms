package Oct.ex_22102024_WhileLoop;
public class Lab110 {
    public static void main(String[] args) {
        int a1=0;
        while(a1<0)
        {
            System.out.println("Run code: " +a1);
            a1++;
        }
        int a2=0;
        do
        {
            System.out.println("Run code: " +a2);
            a2++;
        }while(a2<0);
    }

}
